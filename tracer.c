#include "fdf.h"

void	tracer(t_map init, t_vec2 v1, t_vec2 v2)
{
	t_pt	p;

	p.x_1 = v1.x + init.u_x;
	p.x_2 = v2.x + init.u_x;
	p.y_1 = v1.y + init.u_y;
	p.y_2 = v2.y + init.u_y;
	line(init, p);
}
